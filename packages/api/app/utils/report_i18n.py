SUPPORTED_LOCALES = frozenset(LABELS.keys())
DEFAULT_LOCALE = "en"


def normalize_locale(locale: str | None) -> str:
    """Map a user-stored locale string onto a key in `LABELS`.

    Accepts:
      - the canonical 2-letter codes we ship (en/it/fr/de/es)
      - regional variants (`en-US`, `it-IT`, `pt-BR`, ...) — strip the
        regional suffix and try the language base
      - None / unknown → fall back to en

    The User model currently stores 2-letter codes from the FE picker,
    so the regional-variant branch is defensive against future
    expansion (e.g. an /auth/me PATCH that accepts `pt-BR`).
    """
    if not locale:
        return DEFAULT_LOCALE
    code = locale.lower().split("-", 1)[0].split("_", 1)[0]
    return code if code in SUPPORTED_LOCALES else DEFAULT_LOCALE


def t(locale: str | None, key: str) -> str:
    """Translate `key` for the given `locale`.

    Falls back to English if either:
      - the locale is not supported (defensive — see `normalize_locale`),
      - the key is missing in the requested locale (typo / new key
        without translation; the unit tests catch these but we belt-
        and-braces it at runtime so a misspelling doesn't crash a
        report job).

    A truly unknown key (not present even in `en`) returns the key
    itself so the report still renders — the missing label shows up
    as a literal placeholder which makes the bug obvious to whoever
    reviews the document.
    """
    locale_key = normalize_locale(locale)
    bundle = LABELS.get(locale_key, LABELS[DEFAULT_LOCALE])
    if key in bundle:
        return bundle[key]
    return LABELS[DEFAULT_LOCALE].get(key, key)
