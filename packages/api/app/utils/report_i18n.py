"""
v2.4.21 audit reports-006/007 — locale-aware labels for generated reports.

The report renderers in `app/tasks/report_tasks.py` (PDF, HTML, Markdown,
JUnit, CSV) are server-side jobs that run inside the Celery worker — they
cannot reach next-intl, which is a Next.js client-side library. Pre-v2.4.21
that meant every "NIS2 Compliance Scan Report", "Findings", "Severity",
"Executive Summary" header was hardcoded English regardless of the user's
locale, which is a poor experience for a customer-facing compliance
deliverable in IT/FR/DE/ES tenants.

Why a hand-rolled dictionary instead of Babel / gettext / a templating
engine:

  - **No new runtime dependency.** Babel pulls 45MB+ of CLDR data and a
    full datetime localizer when all we actually need is ~30 label
    translations per locale. The cost/benefit doesn't pencil out.
  - **Self-contained.** The web FE already has `messages/*.json` for its
    own i18n; mirroring that file format here would couple the worker to
    the FE bundle, which is a layering smell. Reports renderers are a
    server-side concern with their own (much smaller) string surface.
  - **Diff-friendly.** A hand-rolled dict in this file makes a "translate
    the new key in IT/FR/DE/ES" PR a 5-line change reviewers can read at
    a glance, instead of hunting through a JSON tree.

Locale fallback: anything not in the requested locale falls back to `en`.
A `None` / unknown locale string also falls back to `en`. So an exotic
locale code (e.g. `pt`, `zh`, ...) doesn't break the renderer — it just
gets the English text, which is the safest possible failure mode for a
compliance document.

Timestamps are intentionally NOT localised. We keep ISO `2026-04-28 14:30
UTC` because:
  - "UTC" is unambiguous across timezones; a Tokyo team reading the
    report knows exactly what the timestamp refers to.
  - Compliance auditors often correlate report timestamps with logs from
    other tools (Splunk, ELK, cloud audit trails) which all standardise
    on UTC ISO. Forcing those readers to mentally convert "28 aprile alle
    14:30" back into ISO is worse than the original problem.
  - User-timezone localisation would require a new `User.timezone` field;
    out of scope.

The audit (reports-008) was specifically about UTC being confusing to a
Tokyo team. We address that by keeping the explicit `UTC` suffix on every
timestamp and translating only the surrounding noun phrase (`Date`,
`Generated by`).
"""

